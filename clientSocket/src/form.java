    private static void startClient(){
    try{
      client = new Socket("127.0.0.1", 3334);
    }
    catch(IOException ex){
       System.out.println("Deu erro, não possível conectar no servidor");
    }
    }
    private static void sendmessage(String msg) throws IOException{
        PrintStream output;
        try{
        output = new PrintStream(client.getOutputStream());
        output.println(msg);
        }
        catch(IOException ex){
          System.out.println("Deu erro, não possível enviar mensagem ao servidor");
        }
    }
